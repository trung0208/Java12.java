public class Book {
    public int bookCode;
    public String nameBook;
    public String author;
    public String category;
    public String conpany;
    public int year;

    public Book() {
    }

    public Book(int bookCode, String nameBook, String author, String category, String conpany, int year) {
        this.bookCode = bookCode;
        this.nameBook = nameBook;
        this.author = author;
        this.category = category;
        this.conpany = conpany;
        this.year = year;
    }
}
