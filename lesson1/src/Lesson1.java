public class Lesson1 {
    public static void main(String[] args) {
        // sự khác biệt giữa ++i và i++ là i++ tăng giá trị i lên 1 và trả về giá trị mới,còn ++i cũng tăng giá trị i lên 1 nhưng trả về giá trị ban đầu i.
        int i = 5;
        int j = ++i;
        System.out.println(i + "- " + j);






    }
}
