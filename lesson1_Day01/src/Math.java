public class Math {
    public static void main(String[] args) {
        int a = 15;
        int b = 5;

        int sum = a + b;
        System.out.println("a cộng b bằng :" + sum);

        int division = a / b;
        System.out.println("a chia b bằng :" + division);

        int subtraction = a - b;
        System.out.println("a trừ b bằng :" + subtraction);

        int multi = a * b;
        System.out.println("a nhân b bằng :" + multi);

        int subtractionba = b - a;
        System.out.println("b trừ a bằng :" + subtractionba);

        double divisionba = (double) b / a;
        System.out.println("b chia a bằng :" +(double)divisionba);
    }
}
